import type { Festival, Genre, Country, Artist } from "../../types";
import { slugify } from "../../utils";
import { COUNTRY_LABELS } from "../../constants";

interface RawFestival {
  name: string;
  city: string;
  region?: string;
  country: Country;
  lat: number;
  lng: number;
  startDate: string;
  endDate: string;
  genres: Genre[];
  headliners: string[];
  website?: string;
  imageUrl?: string;
}

function toFestival(raw: RawFestival): Festival {
  const slug = slugify(`${raw.name}-${raw.city}-2026`);
  const artists: Artist[] = raw.headliners.map((name) => ({
    id: slugify(name),
    name,
    genres: [],
  }));

  return {
    id: `man-${slug}`,
    name: raw.name,
    slug,
    startDate: raw.startDate,
    endDate: raw.endDate,
    venue: {
      name: raw.city,
      city: raw.city,
      region: raw.region,
      country: raw.country,
      countryName: COUNTRY_LABELS[raw.country],
      coordinates: { lat: raw.lat, lng: raw.lng },
    },
    genres: raw.genres,
    lineup: artists,
    headliners: artists.slice(0, 5),
    imageUrl: raw.imageUrl,
    images: raw.imageUrl ? [raw.imageUrl] : [],
    websiteUrl: raw.website,
    source: "manual",
    sourceId: slug,
    lastUpdated: "2026-02-08T00:00:00.000Z",
    searchableText: [raw.name, raw.city, ...raw.headliners, ...raw.genres]
      .join(" ")
      .toLowerCase(),
  };
}

